package demo;

public class Order {

	private String id;
	private String firstName;
	private String orderDate;
	private Customer customer;

	public String getId() {
		return id;
	}

	public String getFirstName() {
		return firstName;
	}

	public String getOrderDate() {
		return orderDate;
	}

	public Customer getCustomer() {
		return customer;
	}

}
